public class index_of {
    public static void main(String[] args) {
        String str = "Samrat Sinha";
        System.out.println(str.indexOf('a')); // 1
        System.out.println(str.indexOf('z')); // -1
        System.out.println(str.lastIndexOf('a')); // 11
        
        String a = "abc";
        String b = "dbc";
        System.out.println(a.compareTo(b));  //-3
    }
}
