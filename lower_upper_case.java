public class lower_upper_case {
    public static void main(String[] args) {
        String str = "Dekh Kaun Aaya Wapis";
        System.out.println(str.toLowerCase());// dekh kaun aaya wapis
        System.out.println(str.toUpperCase());// DEKH KAUN AAYA WAPIS

        String a = "Upper";
        String b = "Lower";
        System.out.println(a.concat(b)); // UpperLower
    }
}
